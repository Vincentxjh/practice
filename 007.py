#第七个练习-判断是否是打折时间

#显示活动信息，并提示输入星期、时间
print("本店正在进行打折促销活动，活动正在火热进行中……")
week = input("请输入中文星期（如星期一）：" )
time = int(input("请输入当前时间（如0-24）："))

#判断是否满足打折的时间条件
if((week == "星期二" and 10 <= time <= 11) or (week == "星期五" and 14 <= time <= 15)):
    print("恭喜您，获得了参加折扣活动的资格！")
else:
    print("对不起，您来晚一步，期待下次活动……")