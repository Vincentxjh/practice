#第六个练习-使用比较运算符比较大小

#给定三个变量并赋值,输出变量的值
python = 98
english = 92
c = 87
print("Python = " + str(python) + " English = " + str(english) + " C = " + str(c))

#使用比较运算符比较它们之间的大小
print("Python < English ? " + str(python < english))
print("Python > English ? " + str(python > english))
print("Python = English ? " + str(python == english))
print("Python != English ? " + str(python != english))
print("Python <= C ? " + str(python <= c))
print("English >= C ? " + str(english >= c))


